# ACTIVITY 2: DATA TYPES

# Sample variables of different data types
string = "Marc"
number =  25
decimal = 25.2
bool = True
list = ["Hilarie", "Joesper", "Richford", "Roniel", "Anthony"]

# type(variable_name) is used to check the data type of a variable
print("Name:", string, "- Data Type:", type(string))
print("Number:", number, "- Data Type:", type(number))
print("Decimal:", decimal, "- Data Type:", type(decimal))
print("Boolean:", bool, "- Data Type:", type(bool))
print("Students:", list, "- Data Type:", type(list))
